// Generates a WebP sibling for every gallery JPEG and records intrinsic sizes in
// shared/data/imageDimensions.ts.
//
// Run `npm run images:build` after adding or replacing gallery images, then commit
// both the .webp files and the regenerated manifest. Amplify never runs this — it
// only consumes the committed output, so `sharp` stays a local dev dependency.
//
// Why both artifacts matter:
//   - WebP cuts the transfer size of these photos by roughly a third.
//   - Intrinsic width/height on the <img> reserves the box before the bytes land,
//     which is what keeps Cumulative Layout Shift at zero.
import { readdir, stat, writeFile } from 'node:fs/promises'
import { fileURLToPath } from 'node:url'
import sharp from 'sharp'

const GALLERY_DIR = new URL('../public/galleries/', import.meta.url)
const OUT_FILE = new URL('../shared/data/imageDimensions.ts', import.meta.url)
// 74 keeps these photographs visually clean while clearing Lighthouse's
// compression threshold; raise it if you ever see banding in the gradients.
const WEBP_QUALITY = 74

const dirs = (await readdir(GALLERY_DIR, { withFileTypes: true }))
  .filter(entry => entry.isDirectory())
  .map(entry => entry.name)
  .sort()

const manifest = {}
let jpegBytes = 0
let webpBytes = 0

for (const dir of dirs) {
  const dirUrl = new URL(`${dir}/`, GALLERY_DIR)
  const files = (await readdir(dirUrl))
    .filter(name => name.endsWith('.jpg'))
    .sort()

  for (const file of files) {
    const jpegPath = fileURLToPath(new URL(file, dirUrl))
    const webpPath = jpegPath.replace(/\.jpg$/, '.webp')

    const image = sharp(jpegPath)
    const { width, height } = await image.metadata()

    const { size } = await image.webp({ quality: WEBP_QUALITY, effort: 6 }).toFile(webpPath)

    jpegBytes += (await stat(jpegPath)).size
    webpBytes += size

    // Keyed by the WebP path, since that is what the gallery actually serves.
    manifest[`/galleries/${dir}/${file.replace(/\.jpg$/, '.webp')}`] = { width, height }
  }
}

const entries = Object.entries(manifest)
  .map(([path, { width, height }]) => `  '${path}': { width: ${width}, height: ${height} },`)
  .join('\n')

const contents = `// GENERATED by scripts/build-gallery-images.mjs — do not edit by hand.
// Run \`npm run images:build\` to refresh after changing gallery images.
export interface ImageDimensions {
  width: number
  height: number
}

export const imageDimensions: Record<string, ImageDimensions> = {
${entries}
}

export const getImageDimensions = (src: string): ImageDimensions | undefined => imageDimensions[src]
`

await writeFile(OUT_FILE, contents)

const mb = bytes => (bytes / 1024 / 1024).toFixed(2)
console.log(`Processed ${Object.keys(manifest).length} images across ${dirs.length} galleries.`)
console.log(`JPEG ${mb(jpegBytes)} MB → WebP ${mb(webpBytes)} MB (${Math.round((1 - webpBytes / jpegBytes) * 100)}% smaller)`)
